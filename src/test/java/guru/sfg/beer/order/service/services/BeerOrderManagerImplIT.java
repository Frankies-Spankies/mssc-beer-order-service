        await().untilAsserted(() -> {
            BeerOrder foundOrder = beerOrderRepository.findById(beerOrder.getId()).get();
            assertEquals(BeerOrderStatusEnum.ALLOCATION_EXCEPTION, foundOrder.getOrderStatus());
        });

        //recive un mensaje y lo convierte, en este caso es suficiente conocer el mensaje ya que no se va a ejecutar ninguna accion especifica el recivirse
